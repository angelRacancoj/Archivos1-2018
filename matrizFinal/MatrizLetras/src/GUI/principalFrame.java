    private void setPixel(Container panel, int matrixLong) {
        Dimension panelDimension = new Dimension(letterC.matrixSize * matrixLong, letterC.matrixSize * letterC.PixelSize);
        panel.setSize(panelDimension);
        panel.setPreferredSize(panelDimension);
        panel.setMaximumSize(panelDimension);

        Insets insets = panel.getInsets();
        for (int y = 0; y < letterC.matrixSize; y++) {
            for (int x = 0; x < letterC.matrixSize * matrixLong; x++) {
                panel.add(matrixM.getButtonList().get(y).get(x).getBoton());
                matrixM.getButtonList().get(y).get(x).getBoton().setBounds(x * letterC.PixelSize + insets.left, y * letterC.PixelSize + insets.top, letterC.PixelSize, letterC.PixelSize);
            }
        }
        System.out.println("Matriz agregada");
    }

